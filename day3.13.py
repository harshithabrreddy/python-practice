#Remove of an element
list=[10,20,30,40]
list.remove(10)
print(list)
#Reverse of an element
list=[10,20,30,40]
list.reverse()
print(list)
#Assending order of an elements
list=[5,8,2,3,1]
list.sort()
print(list)
#Decending order of an element
list=[5,8,2,3,1]
list.sort()
list.reverse()
print(list)
