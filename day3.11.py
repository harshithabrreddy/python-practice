#Built-in  List Functions
list=[50,40,30,20,10]
print(f"original List:{list}")
print(f"length of list:{len(list)}")
print(f"maximum Value:{max(list)}")
print(f"minimum Value :{min(list)}")
print(f"sorted List:{sorted(list)}")
print(f"summation:{sum(list)}")
print(any(list))
print(all(list))