# Python program to concatenate two lists and sort in ascending order
list1 = [10, 3, 25, 7, 15]
list2 = [20, 1, 8, 30, 12]
combined_list = list1 + list2
# Sort the combined list
combined_list.sort()
print("First list:", list1)
print("Second list:", list2)
print("Concatenated and sorted list:", combined_list)